// Initialize Firebase
const app = initializeApp(firebaseConfig);
const db = getFirestore(app);

async function uploadAcSlpToFirestore() {
  console.log('ðŸš€ Starting D2D AC & SLP Upload to Firestore\n');
  
  try {
    // Step 1: Read valid entries file
    console.log('ðŸ“Š Reading d2d_valid_entries.json...');
    const validEntriesPath = path.join(__dirname, 'output', 'd2d_valid_entries.json');
    
    if (!fs.existsSync(validEntriesPath)) {
      throw new Error('d2d_valid_entries.json not found. Run import script first.');
    }
    
    const validEntries = JSON.parse(fs.readFileSync(validEntriesPath, 'utf8'));
    console.log(`   âœ… Loaded ${validEntries.length} total entries\n`);
    
    // Step 2: Filter for AC and SLP only
    console.log('ðŸ” Filtering for AC and SLP entries only...');
    const acSlpEntries = validEntries.filter(entry => 
      entry.role === 'AC' || entry.role === 'SLP'
    );
    
    const stats = {
      ac: acSlpEntries.filter(e => e.role === 'AC').length,
      slp: acSlpEntries.filter(e => e.role === 'SLP').length,
      total: acSlpEntries.length
    };
    
    console.log(`   âœ… Filtered entries:`);
    console.log(`      â€¢ AC:  ${stats.ac}`);
    console.log(`      â€¢ SLP: ${stats.slp}`);
    console.log(`      â€¢ Total: ${stats.total}\n`);
    
    // Step 3: Remove _docId field from each entry
    console.log('ðŸ”§ Preparing entries for upload (removing _docId)...');
    const entriesToUpload = acSlpEntries.map(entry => {
      const { _docId, ...entryWithoutDocId } = entry;
      return entryWithoutDocId;
    });
    console.log(`   âœ… Prepared ${entriesToUpload.length} entries\n`);
    
    // Step 4: Upload in batches
    console.log('ðŸ“¤ Starting batch upload to d2d_members collection...');
    const batchSize = 500; // Firestore batch limit
    const batches = [];
    
    for (let i = 0; i < entriesToUpload.length; i += batchSize) {
      batches.push(entriesToUpload.slice(i, i + batchSize));
    }
    
    console.log(`   ðŸ“¦ Created ${batches.length} batches (${batchSize} docs per batch)\n`);
    
    let uploadedCount = 0;
    let batchNumber = 0;
    
    for (const batch of batches) {
      batchNumber++;
      console.log(`   â³ Processing batch ${batchNumber}/${batches.length} (${batch.length} docs)...`);
      
      const firestoreBatch = writeBatch(db);
      
      for (const entry of batch) {
        // Create new document reference with auto-generated ID
        const docRef = doc(collection(db, 'd2d_members'));
        firestoreBatch.set(docRef, entry);
      }
      
      try {
        await firestoreBatch.commit();
        uploadedCount += batch.length;
        console.log(`      âœ… Batch ${batchNumber} committed successfully (${uploadedCount}/${entriesToUpload.length} total)`);
      } catch (error) {
        console.error(`      âŒ Batch ${batchNumber} failed:`, error.message);
        throw error;
      }
      
      // Small delay between batches to avoid rate limiting
      if (batchNumber < batches.length) {
        await new Promise(resolve => setTimeout(resolve, 100));
      }
    }
    
    // Step 5: Generate summary report
    console.log('\n' + '='.repeat(70));
    console.log('ðŸ“Š UPLOAD SUMMARY');
    console.log('='.repeat(70));
    console.log(`\nâœ… Successfully uploaded ${uploadedCount} entries to Firestore:`);
    console.log(`   â€¢ AC entries:  ${stats.ac}`);
    console.log(`   â€¢ SLP entries: ${stats.slp}`);
    console.log(`   â€¢ Total:       ${stats.total}`);
    console.log(`\nðŸ“ Collection: d2d_members`);
    console.log(`ðŸ“ Document IDs: Auto-generated by Firestore`);
    console.log(`ðŸ·ï¸  Parent Vertical: shakti-abhiyaan`);
    
    console.log('\n' + '='.repeat(70));
    console.log('âœ… Upload completed successfully!');
    console.log('='.repeat(70));
    
    console.log('\nðŸ’¡ Next steps:');
    console.log('   1. Verify entries in Firebase Console');
    console.log('   2. Check d2d_members collection for uploaded data');
    console.log('   3. Test UI fetching with fetchD2DMembers.ts');
    console.log('   4. Upload Saathi entries separately if needed\n');
    
  } catch (error) {
    console.error('\nâŒ Error during upload:', error);
    process.exit(1);
  }
}

